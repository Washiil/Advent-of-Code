use regex::Regex;

pub fn part_one(input: &str) -> Option<u32> {
    let re = Regex::new(r"mul\((\d+),(\d+)\)").unwrap();

    let mut output = 0;
    for cap in re.captures_iter(input) {
        let arg1 = &cap[1];
        let arg2 = &cap[2];
        output += arg1.parse::<u32>().unwrap() * arg2.parse::<u32>().unwrap()
    }

    Some(output)
}

